package edu.illinoisstate.robert.recs;

public class Rec2 {

    /*
    Rec 2 numeric promotion behavior
     */
    public static void main(String[] args) {
        double a = 10.0;
        int b = 14;

        float c = 11.0f;
        int d = 15;

        long e = 12;
        int f = 16;

        int g = 13;
        int h = 17;

        System.out.println(a + b);
        System.out.println(c + d);
        System.out.println(e + f);
        System.out.println(g + h);
    }

}
